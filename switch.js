var color ='sky';
if(color == 'white')
{
    console.log('color is white');
}
else if( color == 'red')
{
    console.log('color is red');
}
else if(color == 'green')
{
    console.log('color is green');
}
else{
    console.log('color is black');
}


//switch case
switch(color)
{
    case 'green':
        console.log('color is green');
        break;
    
  case 'red' :
        console.log('color is red');
        break;

    case 'white' :
        console.log('color is white');
        break;
    
    default :
        console.log('color is sky');
}